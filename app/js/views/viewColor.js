function getViewColor(view) {
   if (view == "home")
      return "#fb8c00ba";
   else if (view == "web")
      return "rgb(0, 122, 250)";
   else if (view == "video")
      return "rgb(255, 59, 48)";
   else if (view == "3d")
      return "rgb(88, 86, 214)";
   else if (view == "design")
      return "rgb(76, 217, 100)";
   else
      return "#fb8c00";
}